// Helper functions
export const getContractAddress = (chainId: number, contractName: string): string | undefined => {
  const network = Object.values(BUSINESS_CONTRACTS).find(n => n.chainId === chainId);
  return network?.contracts[contractName as keyof typeof network.contracts];
};

export const isSupportedChain = (chainId: number): boolean => {
  return Object.values(BUSINESS_CONTRACTS).some(n => n.chainId === chainId);
};

export const getNetworkInfo = (chainId: number) => {
  return Object.values(BUSINESS_CONTRACTS).find(n => n.chainId === chainId);
};

export const getHubChain = () => {
  return Object.values(BUSINESS_CONTRACTS).find(n => n.isHub);
};

export const getSpokeChains = () => {
  return Object.values(BUSINESS_CONTRACTS).filter(n => !n.isHub);
};

